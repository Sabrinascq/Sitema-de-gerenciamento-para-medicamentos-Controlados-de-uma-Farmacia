    private void carregarMedicamentos() {
    try {
        cxNomeMedicamento.removeAllItems();
        cxNomeMedicamento.addItem("-- Selecione o Medicamento --"); 

        MedicamentoComumDAO comumDao = new MedicamentoComumDAO();
        List<MedicamentoComum> comuns = comumDao.listarTodos();
        for (MedicamentoComum med : comuns) {
            cxNomeMedicamento.addItem(med.getNome());
        }

        MedicamentoControladoDAO controladoDao = new MedicamentoControladoDAO();
        List<MedicamentoControlado> controlados = controladoDao.listarTodos();
        for (MedicamentoControlado med : controlados) {
            cxNomeMedicamento.addItem(med.getNome() + " (Controlado)");
        }
    } catch (Exception e) {
        logger.log(java.util.logging.Level.SEVERE, "Erro ao carregar lista de medicamentos", e);
        JOptionPane.showMessageDialog(this, "Erro ao carregar medicamentos.", "Erro de Banco", JOptionPane.ERROR_MESSAGE);
    }
}

