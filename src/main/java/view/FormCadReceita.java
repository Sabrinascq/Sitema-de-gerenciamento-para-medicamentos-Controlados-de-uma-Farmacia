    private void btCadReceitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadReceitaActionPerformed
   String cpfCliente = cxCPF1.getText().trim();
    String nomeMedCompleto = (String) cxNomeMedicamento.getSelectedItem(); 
    
    int idClienteReal = 0;
    
    ClienteDAO clienteDao = new ClienteDAO();
    Cliente clienteEncontrado = clienteDao.buscarPorCpf(cpfCliente); 

    if (clienteEncontrado == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Cliente não encontrado ou CPF inválido.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        return; 
    }
    
    idClienteReal = 1; 

    
    if (nomeMedCompleto == null || nomeMedCompleto.startsWith("--")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um medicamento válido.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    String nomeMed = nomeMedCompleto.replace(" (Controlado)", "").trim(); 

    
    Receita novaReceita = new Receita();
    novaReceita.setClienteId(idClienteReal);
    novaReceita.setNomeMedicamento(nomeMed);
    novaReceita.setDataEmissao(cxData.getText()); 
    novaReceita.setPosologia(cxPosologia.getText()); 

    ReceitaCtrl ctrl = new ReceitaCtrl();
    ctrl.adiciona(novaReceita);
    }//GEN-LAST:event_btCadReceitaActionPerformed
 
