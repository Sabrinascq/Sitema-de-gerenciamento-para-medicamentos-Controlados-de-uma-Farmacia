    private void btCRMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCRMActionPerformed
        try {
    
    String urlDoCRM = "https://portal.cfm.org.br/busca-medicos/"; 
    
   
    if (java.awt.Desktop.isDesktopSupported()) {
        java.awt.Desktop desktop = java.awt.Desktop.getDesktop();
        
        
        if (desktop.isSupported(java.awt.Desktop.Action.BROWSE)) {
            
            
            java.net.URI uri = new java.net.URI(urlDoCRM);
            desktop.browse(uri);
        } else {
            
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Seu sistema não suporta a abertura automática de links.", 
                "Erro de Navegação", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } else {
        
        javax.swing.JOptionPane.showMessageDialog(this, 
            "A classe Desktop não é suportada neste ambiente.", 
            "Erro de Ambiente", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
} catch (java.io.IOException | java.net.URISyntaxException ex) {
    
    javax.swing.JOptionPane.showMessageDialog(this, 
        "Ocorreu um erro ao tentar abrir o link: " + ex.getMessage(), 
        "Erro", 
        javax.swing.JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_btCRMActionPerformed

