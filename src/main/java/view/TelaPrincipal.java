    private void configurarAcesso(LoginCtrl.TipoUsuario nivel) {
        if (nivel == LoginCtrl.TipoUsuario.FUNCIONARIO) {
            
            mnRel.setVisible(false);
            
            itMnCadAdm.setVisible(false);
            
            rtTelaPrinc.setText("Bem vindo Funcionário!");
        } else if (nivel == LoginCtrl.TipoUsuario.ADMINISTRADOR) {
            rtTelaPrinc.setText("Bem vindo Administrador!");
        }
    }
